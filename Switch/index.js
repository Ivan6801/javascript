var singo = prompt('Cuál es tu signo?')

switch (singo) {
    case 'acuario':
        console.log('En astrología, Aquarios o Acuario es el undécimo signo del zodiaco, el sexto de naturaleza positiva y el cuarto de cualidad fija —junto con Tauro, Leo y Escorpio—.​​ Simboliza la revolución y su símbolo representa la sabiduría al ser.')
        break
    case 'piscis':
        console.log('En astrología, Pisces o Piscis es el duodécimo signo del zodíaco, el sexto de naturaleza negativa y de cualidad mutable.​​Simboliza la disolución y su símbolo representa dos peces nadando en sentidos opuestos. Pertenece, junto a Cáncer y Escorpio al elemento agua.​ Está regido por Neptuno y Júpiter.')
        break
    case 'cancer':
        console.log('Cáncer es el nombre que se da a un conjunto de enfermedades relacionadas. En todos los tipos de cáncer, algunas de las células del cuerpo empiezan a dividirse sin detenerse y se diseminan a los tejidos del derredor.')
        break
}

